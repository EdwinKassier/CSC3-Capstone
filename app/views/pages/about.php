        <h3 class="card-title">About the Black Eagle Project</h3>
        <div class="card card-body" style="text-align: justify;">              
            <p>
                The Black Eagle Project was established in 2011 in collaboration with the Animal
                Demography Unit at the University of Cape Town. The CLT provided a 2year scholarship to PhD
                candidate Megan Murgatroyd, who has since developed the study into a fully fledged research project,
                and obtained her PhD on the subject. The Black Eagle Project aims to assess the effects of land use,
                in particular agriculture, on the diet and hunting habits of the Black eagle (or Verreaux’s eagle,
                Aquila verreauxii). The research is comparing the contrasting habitats and conditions experienced
                between Black eagles in the Cederberg Mountains and those in the Sandveld. The Cederberg remains
                relatively pristine and the Sandveld has undergone extensive land conversion for agriculture; both
                areas are home to viable breeding populations of this eagle. The ultimate aim of this project is to
                furnish the data necessary to raise awareness of the importance of a stable habitat and prey base
                for this apex predator and to use its enigmatic status to create public awareness and involvement.
                The iconic flight of the Verreaux’s eagle is a breathtaking phenomenon. In the Cederberg Mountains
                it is still a common sight to see a pair flying in a unique pendulum formation over the cliffs and
                territory that they defend. However, in other parts of the country their long-term survival might be
                threatened by radical change in land-use. In the Western Cape at present these eagles remain
                prevalent and breeding in the Sandveld. Yet this area is now classed as the second most highly
                threatened ecosystem in South Africa and at least 50% of the land has been converted for agriculture
                (C.A.P.E. 2008; Low 2004).
            </p>
            <p>
                The Black Eagle Project takes an ecosystem approach. As a top predator this eagle is vulnerable to
                perturbations to the environment at any lower trophic level. As an umbrella species, successful
                conservation of the Verreaux’s eagle is dependent on the entire ecosystem remaining healthy. At the
                same time, the conservation of this top predator helps enable the ecosystem to remain healthy by
                preventing trophic downgrading or cascades, the process whereby the loss of a predator enables a
                prey species to increase in abundance in such a way that the stability of the ecosystem is
                disturbed. The distribution of this eagle coincides closely with that of their staple food, the Rock
                hyrax. Habitat change appears to be a cause of declining prey availability for the Verreaux’s eagle.
                Declines in the Rock hyrax are thought to be the major driver behind losses of the Verreaux’s eagle
                from former breeding grounds. For example on the Cape Peninsula the number of breeding pairs has
                gone from at least five to just one in the last 20 years (Jenkins & van Zyl 2009; Rodrigues).
            </p>
